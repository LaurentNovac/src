package ch.supermafia.processing.framework3D.geometry.matrix;


public class Matrix4x4Identity extends Matrix4x4
	{
	/*------------------------------------------------------------------*\
	|*							Constructeurs							*|
	\*------------------------------------------------------------------*/
	
	/*------------------------------------------------------------------*\
	|*							Methodes Public							*|
	\*------------------------------------------------------------------*/
	
	/*------------------------------*\
	|*				Set				*|
	\*------------------------------*/
	
	/*------------------------------*\
	|*				Get				*|
	\*------------------------------*/
	
	/*------------------------------------------------------------------*\
	|*							Methodes Private						*|
	\*------------------------------------------------------------------*/
	protected void fill()
		{
		for(int y = 0; y < dim; y++)
			{
			for(int x = 0; x < dim; x++)
				{
				if (x == y)
					{
					this.data[index(x, y)] = 1.0f;
					}
				else
					{
					this.data[index(x, y)] = 0.0f;
					}
				}
			}
		}
	/*------------------------------------------------------------------*\
	|*							Attributs Private						*|
	\*------------------------------------------------------------------*/
	
	

	}
